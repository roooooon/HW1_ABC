#ifndef HW1_NUMBER_H
#define HW1_NUMBER_H
//------------------------------------------------------------------------------
// number.h - содержит описание обобщённого числа и его интерфейса
//------------------------------------------------------------------------------

#include <iostream>
#include "rnd.h"
#include "fraction.h"
#include "complex_number.h"
#include "polar.h"

struct number {
    // значения ключей для каждого из чисел
    enum key {FRACTION, COMPLEX, POLAR};
    key k; // ключ
    // используемые альтернативы
    union { // используем простейшую реализацию
        polar p;
        fraction f;
        complex_number c;
    };
};
// Случайный ввод обобщенной чисел
bool num_InRnd(number &n);

//------------------------------------------------------------------------------
// Вычисление периметра чисел
double num_calculation(number n);

//------------------------------------------------------------------------------
// Ввод параметров обобщенной чисел из файла
bool num_In(number& n, ifstream &ifst);

//------------------------------------------------------------------------------
// Вывод параметров текущей фигуры в поток
void num_Out(number &n, ofstream &ofst);


#endif //HW1_NUMBER_H
